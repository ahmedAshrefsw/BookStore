<?php

namespace App\Http\Controllers;


use Illuminate\Http\Request;
use App\Book;

class BooksController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $books = Book::all();
        //$book = Book::where('title', 'testawy')->first();
        //dd($book);


        return view('books.index' , ['books' => $books]);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('books.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validData = $request->validate(
            [
                'title' =>'required',
        $validData = $this->validData();

        if(Book::where('title', $request->title)->first()){
            $book = Book::where('title', $request->title)->first();
        }else{
            $book = Book::create($validData);
        }
        $book->numOfProduct++;
        $book->save();
        $this->storeCover($book);


        // \DB::table('books')->insert($validData);

        return redirect('/books');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        return('books.show');
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        return('books.edit');
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        return('books.update');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        return('books.destroy');
    }

    private function validData(){

        return tap(
            request()->validate(
                [
                    'title' =>'required',
                    'author' =>'required',
                    'description' => 'required',
                    'price' =>'required'
                ]),
            function (){
                if (request()->hasFile('cover')){
                    request()->validate(
                        [
                            'cover' => 'File|Image',
                        ]
                    );}}
        );




    }

    private function storeCover($book)
    {
        if (\request()->has('cover')){
            $book-> update([
                'cover' => request()->cover->store('uploads','public')
            ]);
        }
    }
}
